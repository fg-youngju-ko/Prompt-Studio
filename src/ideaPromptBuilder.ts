export interface QuestionAnswer {
  question: string;
  answer: string;
}

export function buildIdeaStartPrompt(idea: string, summary: string): string {
  return `# 아이디어
${idea}

# 프로젝트 설명
${summary}

# 현재 작업 범위
지금은 프로젝트의 첫 번째 MVP만 개발한다. 전체 기능을 한 번에 만들지 않는다.

# 개발 원칙
- MVP 우선 개발: 핵심 기능 하나가 정상 동작하는 것을 최우선으로 한다.
- 기존 구조 분석: 코드를 작성하기 전에 기존 프로젝트 구조와 파일을 먼저 파악한다.
- 영향 범위 확인: 변경 사항이 다른 파일/기능에 미치는 영향을 확인한다.
- 더 좋은 구조 제안: 더 나은 구조나 방법이 있다면 먼저 제안한다.
- 사용자 승인 후 구현: 제안에 대해 사용자 승인을 받은 뒤에 실제 구현을 시작한다.
- 테스트 수행: 구현 후에는 정상 동작 여부를 테스트한다.
- 변경 내용 요약: 작업이 끝나면 무엇을 바꿨는지 간단히 요약한다.

# 요청
위 내용을 바탕으로 이 프로젝트의 첫 번째 MVP를 시작하고 싶습니다. 먼저 구현 계획과 필요한 파일, 영향받는 파일을 설명해 주세요. 제 승인 후에 개발을 시작해 주세요.`;
}
